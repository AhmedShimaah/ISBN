// This method generates all possible ISBNs by replacing X, Y, Z with valid digits
private void generateISBNs(char[] isbnCharacters, int index, List<String> generatedISBNs) {
    if(index == isbnCharacters.length) {
        if(checkValidISBN(isbnCharacters)) {
            generatedISBNs.add(new String(isbnCharacters));
        }
        return;
    }
    if(isbnCharacters[index] == 'X' || isbnCharacters[index] == 'Y' || isbnCharacters[index] == 'Z') {
        for(int j=0; j<=9; j++) {
            isbnCharacters[index] = (char)('0' + j);
            generateISBNs(isbnCharacters, index + 1, generatedISBNs);
        }
        if(index == 9) { // The last character can be 'X'
            isbnCharacters[index] = 'X';
            generateISBNs(isbnCharacters, index + 1, generatedISBNs);
        }
        isbnCharacters[index] = 'X'; // Reset the character back to 'X'
    } else {
        generateISBNs(isbnCharacters, index + 1, generatedISBNs);
    }
}
// This method checks whether the given 10-digit sequence is a valid ISBN
private boolean checkValidISBN(char[] isbnCharacters) {
    int sum = 0;
    for(int i=0; i<10; i++) {
        int digit = (isbnCharacters[i] == 'X') ? 10 : (isbnCharacters[i] - '0');
        sum += digit * (i+1);
    }
    return sum % 11 == 0;
}
