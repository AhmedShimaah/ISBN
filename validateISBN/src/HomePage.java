    private void move(KeyEvent evt, JTextField currentField, JTextField rightField, JTextField leftField){
        if (evt.getKeyCode() == KeyEvent.VK_LEFT) {
            // Move focus to the previous text field
            if (currentField.getCaretPosition() == 0) {
                leftField.requestFocus();
                leftField.setCaretPosition(leftField.getText().length());
            } else {
                currentField.setCaretPosition(currentField.getCaretPosition() - 1);
            }
        } else if (evt.getKeyCode() == KeyEvent.VK_RIGHT) {
            // Move focus to the next text field
            if (currentField.getCaretPosition() == currentField.getText().length()) {
                rightField.requestFocus();
                rightField.setCaretPosition(0);
            } else {
                currentField.setCaretPosition(currentField.getCaretPosition() + 1);
            }
        }
    }
    
