    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    JTextField[] textFields = new JTextField[]{jTextField1, jTextField2, jTextField3, jTextField4, jTextField5, jTextField6, jTextField7, jTextField8, jTextField9, jTextField10};
    char[] isbnCharacters = new char[10];
    boolean foundXYZ = false;
    
    for(int i=0; i<10; i++){
        String text = textFields[i].getText().toUpperCase();
        isbnCharacters[i] = text.charAt(0);
        if(text.equals("X") || text.equals("Y") || text.equals("Z")) {
            foundXYZ = true;
        }
    }
    
    if(foundXYZ) {
        List<String> generatedISBNs = new ArrayList<>();
        generateISBNs(isbnCharacters, 0, generatedISBNs);
        // Display the generated ISBNs
        if(generatedISBNs.size() > 1) {
            JOptionPane.showMessageDialog(this, "Multiple valid ISBNs generated:\n" + String.join("\n", generatedISBNs));
        } else if(generatedISBNs.size() == 1) {
            JOptionPane.showMessageDialog(this, "Valid ISBN generated:\n" + generatedISBNs.get(0));
            clearAllTextFields();
        } else {
            JOptionPane.showMessageDialog(this, "No valid ISBN could be generated.");
        }
    } else {
        // Display error when no X, Y, Z are found in the sequence
        JOptionPane.showMessageDialog(this, "Error: Enter X, Y, or Z");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

