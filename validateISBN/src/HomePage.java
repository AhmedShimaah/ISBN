    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          // Get the values from the text fields
    int[] digits = new int[10];
    try {
        digits[0] = Integer.parseInt(jTextField1.getText());
        digits[1] = Integer.parseInt(jTextField2.getText());
        digits[2] = Integer.parseInt(jTextField3.getText());
        digits[3] = Integer.parseInt(jTextField4.getText());
        digits[4] = Integer.parseInt(jTextField5.getText());
        digits[5] = Integer.parseInt(jTextField6.getText());
        digits[6] = Integer.parseInt(jTextField7.getText());
        digits[7] = Integer.parseInt(jTextField8.getText());
        digits[8] = Integer.parseInt(jTextField9.getText());
        digits[9] = Integer.parseInt(jTextField10.getText());
    } catch (NumberFormatException e) {
        // Invalid input, display an error message
        JOptionPane.showMessageDialog(this, "Invalid input. Please enter only digits 0-9 or X for the last digit.", "Error", JOptionPane.ERROR_MESSAGE);
        clearAllTextFields();
        return;
    }

    // Check if the digits are in the range 0-9
    for (int i = 0; i < 9; i++) {
        if (digits[i] < 0 || digits[i] > 9) {
            // Invalid input, display an error message
            JOptionPane.showMessageDialog(this, "Invalid input. Please enter only digits 0-9 for the first 9 digits, where the first digit is 1 or 0", "Error", JOptionPane.ERROR_MESSAGE);
            clearAllTextFields();
            return;
            
        }
    }

    if (digits[9] < 0 || digits[9] > 10 || (digits[9] == 10 && jTextField10.getText().charAt(0) != 'X')) {
        // Invalid input, display an error message
        JOptionPane.showMessageDialog(this, "Invalid input. The last digit should be 0-9 or X.", "Error", JOptionPane.ERROR_MESSAGE);
        clearAllTextFields();
        return;
    }

    // Validate the ISBN number
    int sum = 0;
    for (int i = 0; i < 9; i++) {
        sum += digits[i] * (10 - i);
    }

    int checkDigit = 0;
    if(jTextField10.getText().charAt(0) == 'X') {
        checkDigit = 10;
    } else {
        checkDigit = digits[9];
    }

    if ((sum + checkDigit) % 11 == 0) {
        JOptionPane.showMessageDialog(this, "Valid ISBN number.", "Success", JOptionPane.INFORMATION_MESSAGE);
        clearAllTextFields();
    } else {
        JOptionPane.showMessageDialog(this, "Invalid ISBN number.", "Error", JOptionPane.ERROR_MESSAGE);
        clearAllTextFields();
    }

    }//GEN-LAST:event_jButton2ActionPerformed

